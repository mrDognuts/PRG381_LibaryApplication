    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
            int borrowID;
            int bookID;
            try {
                borrowID = Integer.parseInt(txtBorowersID.getText().trim());
                bookID = Integer.parseInt(txtBookID.getText().trim());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid Book ID or borrower ID");
                return;
            }  
            try {
                // Connect to db
                conect.connect();

                // Record the borrowing
                Date borrowDate = new Date(System.currentTimeMillis());
                conect.recordBorrowing(borrowID, bookID, (java.sql.Date) borrowDate);

                // Refresh the table to show the updated borrowing history
                DefaultTableModel model = (DefaultTableModel) tblHistory.getModel();
                model.setRowCount(0);

                for (String[] row : conect.viewBorrowingHistory(borrowID)) {
                    model.addRow(row);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            }
    }//GEN-LAST:event_btnAddActionPerformed

