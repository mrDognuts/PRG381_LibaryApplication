    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblHistory.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a record to delete.");
            return;
        }

        int borrowingID = Integer.parseInt(tblHistory.getValueAt(selectedRow, 0).toString());
        int borrowerID = Integer.parseInt(txtBorowersID.getText().trim());

        try {
            // Connect to db
            conect.connect();

            // Delete the borrowing record
            conect.deleteBorrowing(borrowingID);

            // Refresh the table to show the updated borrowing history
            DefaultTableModel model = (DefaultTableModel) tblHistory.getModel();
            model.setRowCount(0);

            for (String[] row : conect.viewBorrowingHistory(borrowerID)) {
                model.addRow(row);
            }

            JOptionPane.showMessageDialog(this, "Record deleted successfully.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

