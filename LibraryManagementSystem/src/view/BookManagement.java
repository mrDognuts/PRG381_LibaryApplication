    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    // Get input values
    String title = txtTitle.getText().trim();
    String author = txtAuthor.getText().trim();
    String genre = "";
    String publisher = txtPublisher.getText().trim();
    
    // Validate input fields
    if (title.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Title is required.");
        return;
    }
    
    if (author.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Author is required.");
        return;
    }
    
    if (publisher.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Publisher is required.");
        return;
    }
    
    // Check which radio button is selected for genre
    if (rbFiction.isSelected()) {
        genre = "Fiction";
    } else if (rbNonFic.isSelected()) {
        genre = "Non-Fiction";
    } else if (rbYoung.isSelected()) {
        genre = "Young Adult";
    } else if (rbChild.isSelected()) {
        genre = "Children";
    } else if (rbGraphic.isSelected()) {
        genre = "Graphic Novel";
    } else if (rbPoetDrama.isSelected()) {
        genre = "Poetry/Drama";
    } else {
        JOptionPane.showMessageDialog(this, "Genre is required.");
        return;
    }
    
    // Convert Book ID to an integer
    int bookID;
    try {
        bookID = Integer.parseInt(txtBookID.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid Book ID.");
        return;
    }

    // Update the book details in the database
    conect.updateBook(bookID, title, author, genre, publisher);
    JOptionPane.showMessageDialog(this, "Book updated successfully.");
    
    // Refresh the table to display updated data
    refreshBookTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Convert Book ID to an integer
        int bookID;
        try {
            bookID = Integer.parseInt(txtBookID.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Book ID.");
            return;
        }
        conect.deleteBook(bookID);
        refreshBookTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewSearchActionPerformed
    String bookIDStr = txtBookID.getText().trim();
    int bookID;
    try {
        bookID = Integer.parseInt(bookIDStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid Book ID.");
        return;
    }
    
    // Call the search method
    String[] book = conect.searchBookById(bookID);
    if (book != null) {
        // Set the text fields with the search result
        txtTitle.setText(book[1]);
        txtAuthor.setText(book[2]);
        txtPublisher.setText(book[4]);
        // Select the genre radio button based on the search result
        selectGenreRadioButton(book[3]);
        selectBookRowInTable(bookID);
    } else {
        JOptionPane.showMessageDialog(this, "Book not found.");
    }
    }//GEN-LAST:event_btnViewSearchActionPerformed
    private void refreshBookTable() {
    ArrayList<String[]> books = conect.viewBooks();
    DefaultTableModel model = (DefaultTableModel) tblBook.getModel();
    model.setRowCount(0); // Clear the table
    
    for (String[] book : books) {
        model.addRow(book);
        }
    }
    // Selceting corect genre
    private void selectGenreRadioButton(String genre) {
        switch (genre) {
            case "Fiction":
                rbFiction.setSelected(true);
                break;
            case "Non-Fiction":
                rbNonFic.setSelected(true);
                break;
            case "Young Adult":
                rbYoung.setSelected(true);
                break;
            case "Children":
                rbChild.setSelected(true);
                break;
            case "Graphic Novel":
                rbGraphic.setSelected(true);
                break;
            case "Poetry/Drama":
                rbPoetDrama.setSelected(true);
                break;
        }
    }
    private void selectBookRowInTable(int bookID) {
    for (int i = 0; i < tblBook.getRowCount(); i++) {
        if (Integer.parseInt(tblBook.getValueAt(i, 0).toString()) == bookID) {
            tblBook.setRowSelectionInterval(i, i);
            break;
        }
    }
}
